package com.ciandt.hackathon.resources;

public class TopTop {

	private String name;

	private Double valor;

	private String poder;

	private int pontos;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the poder
	 */
	public String getPoder() {
		return poder;
	}

	/**
	 * @param poder
	 *            the poder to set
	 */
	public void setPoder(String poder) {
		this.poder = poder;
	}

	/**
	 * @return the pontos
	 */
	public int getPontos() {
		return pontos;
	}

	/**
	 * @param pontos
	 *            the pontos to set
	 */
	public void setPontos(int pontos) {
		this.pontos = pontos;
	}

	/**
	 * @return the valor
	 */
	public Double getValor() {
		return valor;
	}

	/**
	 * @param valor
	 *            the valor to set
	 */
	public void setValor(Double valor) {
		this.valor = valor;
	}

}
